while True:
    dia = int(input("Digite o n° do dia da semana: "))

    if dia == 1:
        print("Domingo.")
    elif dia == 2:
        print("Segunda")
    elif dia == 3:
        print("Terça.")
    elif dia == 4:
        print("Quarta.")
    elif dia == 5:
        print("Quinta.")
    elif dia == 6:
        print("Sexta.")
    elif dia == 7:
        print("Sábado.")
    

while False:
    if dia != (1,2,3,4,5,6,7):
        print("Dia inválido.")
        print("Escolha um n° de 1 até 7.")